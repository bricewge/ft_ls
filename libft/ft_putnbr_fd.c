#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*intmin;

	intmin = "-2147483648";
	if (n == -2147483648)
	{
		while (*intmin)
			ft_putchar_fd(*intmin++, fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n >= 0 && n <= 9)
		ft_putchar_fd(n + '0', fd);
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + '0', fd);
	}
}
